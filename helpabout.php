About the calendar
</H3>
The event calendar is open to all 
<a href="http://www.uusa.vt.edu/lsop/registration_info.htm">officially registered organizations</a> 
and departments on campus. Events must be open to the university community and those sponsored 
by commercial entities are prohibited.
<P>

<ul><li>Authorization may be obtained by contacting the
<a href="mailto:calendar@vt.edu">calendar coordinator</a> in the 
<a href="http://www.unirel.vt.edu">Office of University Relations</a>. 
<li>All event submissions will be reviewed before posted. 
Consequently, it is important to submit items at least two days in advance of the event. 
<li>During the review process, submissions will be checked to see if they are appropriate 
for posting and edited for clarity and conciseness. It is important to fill in all of the 
fields of information if possible. 
<li>If a submission is rejected, an e-mail will be sent to the sponsor to update the entry. 
</ul>
